    <h1>Что такое JavaScript?</h1>
    <h4>JavaScript — мультипарадигменный язык программирования. Поддерживает объектно-ориентированный, императивный и функциональный стили. Является реализацией стандарта ECMAScript. JavaScript обычно используется как встраиваемый язык для программного доступа к объектам приложений.Тоесть JavaScript(далее JS) можно вставлять напрямую в HTML-код или использовать отдельно.Одним из плюсов JS можно считать то,что он уже установлен на вашем компьютере.Также для JS не обязательны текстовые редакторы.Также у JS есть много популярных фреймворков,что значительно упрощает процесс программирования.</h4><hr>
